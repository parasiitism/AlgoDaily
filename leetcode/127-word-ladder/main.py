"""
    3rd: bi directional BFS + hashtable
    - but this time we change the word to get the next combination instead of finding it from the wordList,
    because there might be a lot of words in the wordList which just have 1 digit difference

    Time    O(N * 26L) for each word, traverse the similar words
    Space   O(N * 26L)
    524 ms, faster than 31.22%
"""


class Solution:
    def ladderLength(self, beginWord: str, endWord: str, wordList: List[str]) -> int:
        wordSet = set(wordList)
        if endWord not in wordSet:
            return 0

        seen1 = {}
        q1 = [(beginWord, 1)]

        seen2 = {}
        q2 = [(endWord, 0)]

        while len(q1) > 0 or len(q2) > 0:

            if len(q1) > 0:
                n = len(q1)
                for _ in range(n):
                    w, steps = q1.pop(0)
                    if w in seen2:
                        return steps + seen2[w]
                    if w in seen1:
                        continue
                    seen1[w] = steps
                    for i in range(len(w)):
                        for c in 'abcdefghijklmnopqrstuvwxyz':
                            cand = w[:i] + c + w[i+1:]
                            if cand in wordSet and cand != w:
                                q1.append((cand, steps + 1))
            if len(q2) > 0:
                n = len(q2)
                for _ in range(n):
                    w, steps = q2.pop(0)
                    if w in seen1:
                        return steps + seen1[w]
                    if w in seen2:
                        continue
                    seen2[w] = steps
                    for i in range(len(w)):
                        for c in 'abcdefghijklmnopqrstuvwxyz':
                            cand = w[:i] + c + w[i+1:]
                            if cand in wordSet and cand != w:
                                q2.append((cand, steps + 1))
        return 0


